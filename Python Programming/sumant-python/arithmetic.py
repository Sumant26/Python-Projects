# numbers

num1 = 10
num2 = 2

# addition +
num1 + num2
addition = num1 + num2 


# subtraction -
subtraction = num1 - num2 
print(subtraction)

# multiplication
product = num1 * num2 
print(product)

# division
division = num1 / num2
print(division)


# division with integer part only //
divisionInt = num1 // num2
print(divisionInt)

# remainder 
# mod == %
remainder = 7 % num2
print(remainder)

# if remainder is 0, number is completely divisible
# remainder is always less than the divisor

# raised to / power. **
power = num1 ** num2
print(power)